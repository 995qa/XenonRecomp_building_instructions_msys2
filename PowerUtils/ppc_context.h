struct PPCRegister
{
    union
    {
        int8_t s8;
        uint8_t u8;
        int16_t s16;
        uint16_t u16;
        int32_t s32;
        uint32_t u32;
        int64_t s64;
        uint64_t u64;
        float f32;
        double f64;
    };
};

struct PPCXERRegister
{
    uint8_t so;
    uint8_t ov;
    uint8_t ca;
};

struct PPCCRRegister
{
    uint8_t lt;
    uint8_t gt;
    uint8_t eq;
    union
    {
        uint8_t so;
        uint8_t un;
    };

    template<typename T>
    void compare(T left, T right, const PPCXERRegister& xer)
    {
        lt = left < right;
        gt = left > right;
        eq = left == right;
        so = xer.so;
    }

    void compare(double left, double right)
    {
        un = __builtin_isnan(left) || __builtin_isnan(right);
        lt = !un && (left < right);
        gt = !un && (left > right);
        eq = !un && (left == right);
    }

    void setFromMask(__m128 mask, int imm)
    {
        int m = _mm_movemask_ps(mask);
        lt = m == imm; // all equal
        gt = 0;
        eq = m == 0; // none equal
        so = 0;
    }

    void setFromMask(__m128i mask, int imm)
    {
        int m = _mm_movemask_epi8(mask);
        lt = m == imm; // all equal
        gt = 0;
        eq = m == 0; // none equal
        so = 0;
    }
};

struct alignas(0x10) PPCVRegister
{
    union
    {
        int8_t s8[16];
        uint8_t u8[16];
        int16_t s16[8];
        uint16_t u16[8];
        int32_t s32[4];
        uint32_t u32[4];
        int64_t s64[2];
        uint64_t u64[2];
        float f32[4];
        double f64[2];
    };
};

struct PPCFPSCRRegister
{
    uint32_t csr;

    uint32_t loadFromHost()
    {
        csr = _mm_getcsr();
        return (0x6C >> ((csr & _MM_ROUND_MASK) >> 12)) & 3;
    }
        
    void storeFromGuest(uint32_t value)
    {
        csr &= ~_MM_ROUND_MASK;
        csr |= ((0x6C >> (2 * (value & 3))) & 3) << 13;
        _mm_setcsr(csr);
    }

    void setFlushMode(bool enable)
    {
        constexpr uint32_t mask = _MM_FLUSH_ZERO_MASK | _MM_DENORMALS_ZERO_MASK;
        uint32_t value = enable ? (csr | mask) : (csr & ~mask);
        
        if (csr != value)
        {
            _mm_setcsr(value);
            csr = value;
        }
    }
};

